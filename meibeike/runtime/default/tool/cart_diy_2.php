{
	$.getJSON("<?php echo IUrl::creatUrl("/block/order_delivery");?>",{"province":province,"total_weight":"<?php echo isset($this->weight)?$this->weight:"";?>","goodsSum":"<?php echo isset($this->sum)?$this->sum:"";?>"},function(content){

